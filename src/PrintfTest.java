
public class PrintfTest {

	public static void main(String[] args) {
		double myDouble = 100;
		
		System.out.printf("Hello %s \n", "Faschal");
		System.out.printf("string %s \n", "Karel");
		System.out.printf("int %d \n", 21);
		System.out.printf("boolean %b \n", true);
		System.out.printf("char %c \n", 'c');
		System.out.printf("double %.2f \n", myDouble);
	}

}
